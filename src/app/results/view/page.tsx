"use client";

import Link from "next/link";
import { use } from "react";
import { useApi } from "@/hooks/use-api";
import { LoadingState } from "@/components/shared/loading-state";
import { ErrorState as UiErrorState } from "@/components/shared/error-state";

type ResultViewPageProps = {
  searchParams: Promise<{
    token?: string;
    preview?: string;
    batchId?: string;
    matric?: string;
  }>;
};

export default function PublicResultViewPage({ searchParams }: ResultViewPageProps) {
  const params    = use(searchParams);
  const token     = params.token;
  const isPreview = params.preview === "true";
  const batchId   = params.batchId;
  const matric    = params.matric;

  // For preview mode we still need to verify the result exists before showing PDF
  const verifyUrl = isPreview && batchId && matric
    ? `/api/results/preview?batchId=${batchId}&matric=${encodeURIComponent(matric)}`
    : token
    ? `/api/results/${token}`
    : "";

  const { data: portalToken, isLoading, error } = useApi<any>(verifyUrl, { immediate: !!verifyUrl });

  if (!token && !isPreview)      return <TokenErrorState type="not_found" />;
  if (isLoading)                  return <LoadingState title="Loading result…" />;
  if (error === "Token expired")  return <TokenErrorState type="expired" />;
  if (error || !portalToken)      return <TokenErrorState type="not_found" />;

  // For preview mode (admin) use the preview PDF endpoint if we have token from DB,
  // otherwise fall back to the token-based PDF endpoint
  const pdfSrc = token ? `/api/results/${token}/pdf` : null;

  return (
    <div className="flex min-h-screen flex-col bg-gray-100">
      {isPreview && (
        <div className="no-print bg-yellow-50 border-b border-yellow-300 px-4 py-2 text-center text-xs font-semibold text-yellow-800">
          Admin Preview — this is how guardians see the result. No portal token was used.
        </div>
      )}

      {pdfSrc ? (
        /* Embed the real PDF generated by pdf-lib using the exact MTU template */
        <div className="flex flex-1 flex-col items-center py-4">
          <iframe
            src={pdfSrc}
            className="w-full rounded shadow-lg"
            style={{ maxWidth: "1100px", height: "calc(100vh - 64px)", border: "none" }}
            title="Examination Result"
          />
          <p className="mt-3 text-xs text-gray-400">
            Use your browser&apos;s Print button (Ctrl+P / ⌘P) to save as PDF.
            Link expires{" "}
            {portalToken.expiresAt
              ? new Date(portalToken.expiresAt).toLocaleDateString("en-NG", {
                  day: "2-digit", month: "long", year: "numeric",
                })
              : "—"}.
          </p>
        </div>
      ) : (
        /* Preview mode: no token, show a message */
        <div className="flex flex-1 items-center justify-center">
          <p className="text-sm text-gray-500">
            PDF preview is only available for dispatched results with a portal token.
          </p>
        </div>
      )}
    </div>
  );
}

function TokenErrorState({ type }: { type: "expired" | "not_found" }) {
  const copy = type === "expired"
    ? { title: "Link Expired",         description: "This result link has expired. Contact the Registry office.", code: "TOKEN_EXPIRED" }
    : { title: "Result Link Not Found", description: "This link does not exist, is malformed, or access has been revoked.", code: "TOKEN_NOT_FOUND" };

  return (
    <div className="min-h-screen bg-background p-4 sm:p-6">
      <UiErrorState title={copy.title} description={copy.description} code={copy.code}
        details="If this link was sent recently, ask the registry to issue a new secure token." />
      <div className="mx-auto mt-4 flex w-full max-w-xl justify-center">
        <Link href="/" className="text-sm font-medium text-primary underline-offset-4 hover:underline">
          Return Home
        </Link>
      </div>
    </div>
  );
}
